function includesAny(text = '', keywords = []) {
  return keywords.some((keyword) => text.includes(keyword));
}

export function inferQuestionIntent(context = '', options = {}) {
  const {
    includeDaily = true,
    includeStudy = true,
    includeHealth = true
  } = options;
  const text = String(context || '').toLowerCase();
  if (!text) return 'general';

  if (includesAny(text, INTENT_KEYWORDS['relationship-repair'])) return 'relationship-repair';
  if (includesAny(text, INTENT_KEYWORDS.social)) return 'social';
  if (includesAny(text, INTENT_KEYWORDS.relationship)) return 'relationship';
  if (includesAny(text, INTENT_KEYWORDS.career)) return 'career';
  if (includesAny(text, INTENT_KEYWORDS.finance)) return 'finance';
  if (includeStudy && includesAny(text, INTENT_KEYWORDS.study)) return 'study';
  if (includeHealth && includesAny(text, INTENT_KEYWORDS.health)) return 'health';
  if (includeDaily && includesAny(text, INTENT_KEYWORDS.daily)) return 'daily';
  return 'general';
}

